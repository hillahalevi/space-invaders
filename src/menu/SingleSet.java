package menu;

/**
 * The type Single set.
 */
public class SingleSet {
    private String message;
    private String key;
    private String path;

    /**
     * Instantiates a new Single set.
     */
    public SingleSet() {

    }

    /**
     * Gets message.
     *
     * @return the message
     */
    public String getMessage() {
        return this.message;
    }

    /**
     * Sets message.
     *
     * @param m the message
     */
    public void setMessage(String m) {
        this.message = m;
    }

    /**
     * Gets key.
     *
     * @return the key
     */
    public String getKey() {
        return this.key;
    }

    /**
     * Sets key.
     *
     * @param k the key
     */
    public void setKey(String k) {
        this.key = k;
    }

    /**
     * Gets level definition path.
     *
     * @return the level definition path
     */
    public String getPath() {
        return this.path;
    }

    /**
     * Sets path.
     *
     * @param p the path
     */
    public void setPath(String p) {
        this.path = p;
    }
}
